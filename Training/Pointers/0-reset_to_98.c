/**
 * reset_to_98 - update the value of a pointer to an int to 98
 * 
 * Despcription: This function takes a pointer to an integer as parameter
 *               and updates the value it points to 98.
 * 
 * @n: The pointer to an int
 * 
 * Return: nothing (void).
 */

void reset_to_98(int *n)
{
    *n = 98;
}